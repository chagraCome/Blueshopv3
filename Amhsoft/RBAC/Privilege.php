class Amhsoft_RBAC_Privilege {

  private $name;
  private $type;

  public function __construct($name = null, $type = null) {
    $this->name = $name;
    $this->type = $type;
  }

  public function getName() {
    return $this->name;
  }

  public function setName($name) {
    $this->name = $name;
  }

  public function getType() {
    return $this->type;
  }

  public function setType($type) {
    $this->type = $type;
  }

}

